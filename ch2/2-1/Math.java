class Math {
    public static void main(String[] args) {
        int a = 12, b = 6;
        System.out.println("addition of 12 and 6 is 18");
        System.out.println("subtraction of 12 and 6 is 6");
        System.out.println("multiplication of 12 and 6 is 72");
        System.out.println("division of 12 and 6 is 2");
        System.out.println("modulo of 12 and 6 is 0");
        System.out.println(a + "+" + b + "=" + (a + b));
        System.out.println(a + "-" + b + "=" + (a - b));
        System.out.println(a + "*" + b + "=" + (a * b));
        System.out.println(a + "/" + b + "=" + (a / b));
        System.out.println(a + "%" + b + "=" + (a % b));

    }
}